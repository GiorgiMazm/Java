package Animals;

public class  Animal {
    public final int legs;
    public final boolean canSpeak;
    public final boolean tail;
    public final String name;

    public Animal(int legs, boolean canSpeak, boolean tail, String name) {
        this.legs = legs;
        this.canSpeak = canSpeak;
        this.tail = tail;
        this.name = name;
    }
}
